package com.example.helloworld;

public class DynamicThreeLineProgram {

    public static void main(String []args){
         int k=5;
        for(int n=1;n<=k;n++){
            System.out.print(" *");
        }System.out.println();
         System.out.print(" ");
         for(int b=2;b<=k;b++){
             System.out.print(" *");
         }System.out.println();
         for(int v=3;v<=k;v++){
             System.out.print(" ");
         }System.out.print(" *");

    }
}
